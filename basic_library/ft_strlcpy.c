#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t len)
{
	size_t	i;

	if (!dest || !src)
		return (0);
	i = 0;
	if (len > 0)
	{
		while (src[i] != '\0' && i < (len - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (ft_strlen(src));
}
